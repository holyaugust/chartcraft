export function getSmartGraphicTemplate(id: string): SmartGraphicTemplate | undefined {
  return SMART_GRAPHIC_TEMPLATES.find((template) => template.id === id)
}

export function createStateFromTemplate(template: SmartGraphicTemplate): SmartGraphicState {
  return {
    templateId: template.id,
    title: template.sampleTitle,
    subtitle: template.sampleSubtitle,
    items: template.sampleItems.map((item) => ({
      title: item.title,
      body: item.body,
      columnKind: item.columnKind,
      flowSteps: item.flowSteps ? [...item.flowSteps] : undefined,
      metrics: item.metrics ? item.metrics.map((m) => ({ ...m })) : undefined,
    })),
    colorSchemeId: DEFAULT_SMART_GRAPHIC_COLOR_SCHEME,
    footerGroupA: template.sampleFooterGroupA ?? '',
    footerGroupB: template.sampleFooterGroupB ?? '',
  }
}

